    using namespace IO;
    n = read<ll>();
    for (int i = 1; i <= n; ++i) {
        num[i] = read<ll>();
        sum ^= num[i];
    }
    if (sum == 0) {
        printf("lose\n");
        return 0;
    }
    for (int i = 1; i <= n; ++i) {
        if ((num[i] ^ sum) < num[i]) {
            ans = num[i] - (num[i] ^ sum);
            pos = i;
            num[i] ^= sum;
            break;
        }
    }
    writesp(ans);
    writeln(pos);
    for (int i = 1; i <= n; ++i)
        writesp(num[i]);
