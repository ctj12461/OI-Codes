typedef long long ll;

constexpr int maxn = 100000;

ll n, ans;

ll phi(ll x) {
    ll res = x;
    for (ll i = 2; i * i <= n; ++i) {
        if (x % i)
            continue;
        res = res / i * (i - 1);
        while (x % i == 0)
            x /= i;
    }
    if (x > 1)
        res = res / x * (x - 1);
    return res;
}

