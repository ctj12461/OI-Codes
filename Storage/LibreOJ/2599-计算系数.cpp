typedef long long ll;

constexpr ll mod = 10007;

ll a, b, k, n, m;

ll power(ll x, ll k) {
    ll res = 1;
    while (k) {
        if (k % 2 == 1)
            res = res * x % mod;
        x = x * x % mod;
        k /= 2;
    }
    return res % mod;
}

ll C(ll n, ll m) {
    ll a = 1, b = 1;
    for (int i = n - m + 1; i <= n; ++i)
        a = a * i % mod;
    for (int i = 1; i <= m; ++i)
        b = b * i % mod;
    return a * power(b, mod - 2) % mod;
}

