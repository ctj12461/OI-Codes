constexpr int maxn = 20;

int n, maxv, c[maxn], p[maxn], l[maxn];

int exgcd(int a, int b, int &x, int &y) {
    if (b == 0) {
        x = 1;
        y = 0;
        return a;
    }
    int d = exgcd(b, a % b, x, y);
    int t = x;
    x = y;
    y = t - x * (a / b);
    return d;
}

bool check(int m) {
    for (int i = 2; i <= n; ++i) {
        for (int j = 1; j < i; ++j) {
            int a = p[i] - p[j], b = c[j] - c[i], x, y, d;
            if (a < 0)
                a = -a, b = -b;
            d = exgcd(a, m, x, y);
            if (b % d)
                continue;
            x = x * (b / d);
            x = (x % (m / d) + (m / d)) % (m / d);
            if (x > l[i] || x > l[j])
                continue;
            return false;
        }
    }
    return true;
}

